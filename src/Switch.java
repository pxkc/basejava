public class Switch {
    public static void main(String[] args) {
        int i = 5;
        switch (i) {
            case 0:
                System.out.println("执行case ）");
                break;
            case 5:
                System.out.println("执行case5");
            case 10:
                System.out.println("是10");
        }
    }
}
